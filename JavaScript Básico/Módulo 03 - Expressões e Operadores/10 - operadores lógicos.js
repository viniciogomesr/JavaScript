// Operadores lógicos
let email = true
let password = false

// AND (e) && (retorna verdadeiro se tudo for verdadeiro)
console.log(email && password)

// OR (ou) || (se uma for verdadeira já é o suficiente para retornar true)
console.log(email || password)

// NOT (negação) ! (inverte o valor da variável, ou seja, ele vai verificar se password false é o oporto de verdadeiro)
console.log(!password)