let value

// Operador de atribuição
value = 1 // o "=" é a atribuição

// Incremento
value += 2
console.log(value)

// Decremento
value -= 1
console.log(value)

// Multiplicar *3
value *= 5
console.log(value)

// Divisão
value /= 2
console.log(value)

// Resto da divisão
value %= 2
console.log(value)

// Potenciação
value = 3
value **= 2
console.log(value)