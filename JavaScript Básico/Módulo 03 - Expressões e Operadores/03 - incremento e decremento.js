let number = 10
// number = number + 1

// Incremento:
number ++ // Soma mais 1 ao number, que nesse caso é 10.
console.log(number)

console.log(number++) // Incrementa após, por isso não mostra no console.
console.log(number)
console.log(++number) // Incrementa antes

// Decremento:
number -- // Suntrai menos 1 ao number, que nesse caso é 10.
console.log(number)

console.log(number--) // Decrementa após, por isso não mostra no console.
console.log(number)
console.log(--number) // Decrementa antes.

// Incrementar mais de um:
number += 10
console.log(number)

// Decrementar mais de um:
number -= 5
console.log(number)