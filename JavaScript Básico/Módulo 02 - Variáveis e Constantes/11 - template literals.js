// Tempolate literals (tamplate strings, interpolação de strings)
let username = "Rodrigo"
let email = "rodrigo@email.com"

// Passar mais de uma parâmetro
console.log(username, email)

// Concatenar texto
let message = "Olá," + username + ". Você conectou com o email: " + email
console.log(message)

// Template literals
console.log(`Olá ${username}, Você conectou com o email: ${email}, seja bem vindo.`)