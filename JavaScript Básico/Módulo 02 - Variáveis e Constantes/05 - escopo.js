// escopo é a região do código onde uma determinada variável é acessível ou visível.

// o escopo define o contexto no qual uma variável pode ser referênciada e modificada.

// tente pensar no contexto como cômodos de uma casa. por exemplo, para utilizar o fogão você deve estar na cozinha.

/*
Tipos de Escopo:

. Global - variáveis declaradas fora de qualquer função ou bloco de código (var)

. Bloco - acessíveis apenas dentro do bloco de código onde foram declaradas (let e const)

. Local - variáveis declaradas dentro de uma função (veremos depois)

analogia ao escopo - a cidade de São Paulo (bloco/local) pertence ao contexto Brasil (global)
*/